package Oprators;

public class BitWise {
    public static void main(String[] args) {
        System.out.println(2&3); // AND 
        System.out.println(2|3); // OR
        System.out.println(2^3); // NOR (Negation)
        System.out.println(~2);  // XOR
        System.out.println(34<<2);  // left shift
        System.out.println(34>>2);  // right shift
        System.out.println(-4<<1);  // left shift
        System.out.println(-4>>1);  // right shift
    }
}
